    "    $shortcut = $WshShell.CreateShortcut(\"$desktopPath\\\\$APP_NAME.lnk\")",
    "    $shortcut.TargetPath = \"cmd.exe\"",
    "    $shortcut.Arguments = \"/c pushd \\\"%USERPROFILE%\\.clubviveverde-tpv\\\" && node electron\\\\tpv-main.js\"",
