    "    Set-ItemProperty -Path $protocolKey -Name \"URL Protocol\" -Value \"\"",
    "",
    "    # Crear clave del comando",
    "    $commandKey = \"$protocolKey\\\\shell\\\\open\\\\command\"",
    "    if (-not (Test-Path $commandKey)) {",
    "        New-Item -Path $commandKey -Force | Out-Null",
    "    }",
    "",
    "    # Configurar comando para ejecutar con Node",
    "    $homePath = $env:USERPROFILE",
    "    $appPath = Join-Path (Get-Location) \"electron\" \"tpv-main.js\"",
    "    $cmdPath = \"$homePath\\.clubviveverde-tpv\"",
    "    $commandValue = \"cmd /c pushd \"\"$cmdPath\"\" && node \"\"$appPath\"\"\"%1\"\"",
    "    Set-ItemProperty -Path $commandKey -Name \"(Default)\" -Value $commandValue",
    "",
    "    Write-Host \"  Protocolo $PROTOCOL:// registrado exitosamente\" -ForegroundColor Green",
    "}",
    "",
    "function Create-Shortcuts {",
    "    Write-Step -Num 6 -Total 6 -Message \"Creando accesos directos...\"",
